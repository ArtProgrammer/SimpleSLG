using System;
using System.Collections.Generic;
using System.IO;

namespace Config {
    class IconsConfigLoader {
        public Dictionary<int, IconsConfig> Datas = new Dictionary<int, IconsConfig> ();

        public Dictionary<int, IconsConfig> LoadConfigData(string str) {
            string[] periods = str.Split('\n');
            int index = 0;
            while (index < periods.Length) {
                string[] split = periods[index].Split(',');
                if (split.Length == 3) {
                    IconsConfig data = new IconsConfig();
                    int.TryParse(split[0], out data.ID);
                    data.Name= split[1];
                    data.Path= split[2];
                    Datas.Add(data.ID, data);
                }
                index++;
                }
            return Datas;
        }
        public IconsConfig GetDataByID(int id) {
            if (Datas.ContainsKey(id)) { 
                return Datas[id];
            }
            return null;
        }
    }
}
